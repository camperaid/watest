'use strict';

const cfg = {
  /**
   * Test run and invocation. If not set, then the testuiste will generate them.
   * Used to collect multiple invocations under a single test run.
   */
  run: process.env.WATEST_RUN,
  invocation: process.env.WATEST_INVOCATION,

  /**
   * Maximum attempts that will be taken to fail a test.
   */
  debugk_limit: process.env.WATEST_DEBUNK_LIMIT,

  /**
   * Logging dir.
   */
  log_dir: process.env.WATEST_LOG_DIR,

  /**
   * If a test takes longer than the given number, the test will fail.
   */
  timeout: process.env.WATEST_TIMEOUT,

  /**
   * Temporary storage dir. Recreated each test run. Shall be used to store
   * files generated by tests if any.
   */
  tmp_dir: process.env.WATEST_TMP_DIR,

  /**
   * Web driver headless mode: true of false.
   */
  webdriver_headless: process.env.WATEST_WEBDRIVER_HEADLESS,

  /**
   * Web driver log level.
   */
  webdriver_loglevel: process.env.WATEST_WEBDRIVER_LOGLEVEL,

  /**
   * Web drivers to run tests for.
   */
  webdrivers: process.env.WATEST_WEBDRIVERS,

  /**
   * Logger module.
   */
  logger: process.env.WATEST_LOGGER_MODULE,

  /**
   * Servicer module.
   */
  servicer: process.env.WATEST_SERVICER_MODULE,
};

module.exports = cfg;
